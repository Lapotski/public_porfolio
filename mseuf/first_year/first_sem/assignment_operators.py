#More To Add to X
x = int(input("Enter x: "))
y = int(input("Enter y: "))
z = int(input("Enter z: "))

yz = y + z
x += yz

print("Result:", x)

#An Increase
price = float(input("Enter the price: "))
price *= 1.1

print("The new price is: %.2f" % price)

#Calculate Distance
kmph = float(input("Enter kilometers per hour: "))
hour = int(input("Enter number of hours traveled: "))
distance = kmph*hour
miles = 0.621371
distance *= miles

print("Distance in miles: %.3f"%distance)
